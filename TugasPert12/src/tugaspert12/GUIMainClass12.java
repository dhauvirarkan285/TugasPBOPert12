    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        JFileChooser chooser = new JFileChooser();
    if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();
        try (BufferedReader br = new BufferedReader(new FileReader(file))) {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPert12PU");
            Depokeretasbi ctrl = new Depokeretasbi(emf);

            String line;
            br.readLine(); // skip header
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                Depokeretasbi d = new Depokeretasbi();
                d.setNomorArmada(data[0]);
                d.setJenisArmada(data[1]);
                d.setTanggalMulaiDinas(java.sql.Date.valueOf(data[2]));
                ctrl.create(d);
            }

            JOptionPane.showMessageDialog(this, "CSV berhasil diunggah!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal unggah: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton5ActionPerformed

