    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Input Depo
            String nomor = JOptionPane.showInputDialog(this, "Masukkan Nomor Armada:");
            if (nomor == null || nomor.isEmpty()) return;
            String jenis = JOptionPane.showInputDialog(this, "Masukkan Jenis Armada:");
            if (jenis == null || jenis.isEmpty()) return;
            String tglDepo = JOptionPane.showInputDialog(this, "Masukkan Tanggal Mulai Dinas (YYYY-MM-DD):");
            if (tglDepo == null || tglDepo.isEmpty()) return;
            // Input Perawatan
            String tglPerawatan = JOptionPane.showInputDialog(this, "Masukkan Tanggal Perawatan (YYYY-MM-DD):");
            if (tglPerawatan == null || tglPerawatan.isEmpty()) return;
            String jenisPerawatan = JOptionPane.showInputDialog(this, "Masukkan Jenis Perawatan:");
            if (jenisPerawatan == null || jenisPerawatan.isEmpty()) return;
            String keterangan = JOptionPane.showInputDialog(this, "Masukkan Keterangan Perawatan:");
            if (keterangan == null) return;
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPert12PU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            Depokeretasbi depo = new Depokeretasbi();
            depo.setNomorArmada(nomor);
            depo.setJenisArmada(jenis);
            depo.setTanggalMulaiDinas(Date.valueOf(tglDepo));
            em.persist(depo);
            em.flush(); // memastikan id_armada tersedia
            PerawatanKereta perawatan = new PerawatanKereta();
            perawatan.setIdArmada(depo);
            perawatan.setTanggalPerawatan(Date.valueOf(tglPerawatan));
            perawatan.setJenisPerawatan(jenisPerawatan);
            perawatan.setKeterangan(keterangan);
            em.persist(perawatan);
            em.getTransaction().commit();
            em.close();
            emf.close();
            JOptionPane.showMessageDialog(this, "✅ Data berhasil disimpan!");
            loadTables(); // reload tabel
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Gagal menyimpan data: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Pilih tabel untuk update
        String[] options = {"Depo", "Perawatan"};
        int choice = JOptionPane.showOptionDialog(this, "Pilih tabel yang ingin diupdate:", "Update",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
        if (choice == -1) return;
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPert12PU");
        EntityManager em = emf.createEntityManager();
        try {
            em.getTransaction().begin();
            if (choice == 0) { // Depo
                int row = jTable1.getSelectedRow();
                if (row == -1) {
                    JOptionPane.showMessageDialog(this, "⚠️ Pilih baris Depo yang ingin diupdate!");
                    return;
                }
                int idDepo = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
                Depokeretasbi depo = em.find(Depokeretasbi.class, idDepo);
                if (depo == null) {
                    JOptionPane.showMessageDialog(this, "⚠️ Data tidak ditemukan!");
                    return;
                }
                String nomor = JOptionPane.showInputDialog(this, "Nomor Armada:", depo.getNomorArmada());
                String jenis = JOptionPane.showInputDialog(this, "Jenis Armada:", depo.getJenisArmada());
                String tgl = JOptionPane.showInputDialog(this, "Tanggal Mulai Dinas (YYYY-MM-DD):", depo.getTanggalMulaiDinas().toString());
                depo.setNomorArmada(nomor);
                depo.setJenisArmada(jenis);
                depo.setTanggalMulaiDinas(Date.valueOf(tgl));
                em.merge(depo);
            } else { // Perawatan
                int row = jTable2.getSelectedRow();
                if (row == -1) {
                    JOptionPane.showMessageDialog(this, "⚠️ Pilih baris Perawatan yang ingin diupdate!");
                    return;
                }
                int idPerawatan = Integer.parseInt(jTable2.getValueAt(row, 0).toString());
                PerawatanKereta perawatan = em.find(PerawatanKereta.class, idPerawatan);
                if (perawatan == null) {
                    JOptionPane.showMessageDialog(this, "⚠️ Data tidak ditemukan!");
                    return;
                }
                String tgl = JOptionPane.showInputDialog(this, "Tanggal Perawatan (YYYY-MM-DD):", perawatan.getTanggalPerawatan().toString());
                String jenis = JOptionPane.showInputDialog(this, "Jenis Perawatan:", perawatan.getJenisPerawatan());
                String ket = JOptionPane.showInputDialog(this, "Keterangan:", perawatan.getKeterangan());
                perawatan.setTanggalPerawatan(Date.valueOf(tgl));
                perawatan.setJenisPerawatan(jenis);
                perawatan.setKeterangan(ket);
                em.merge(perawatan);
            }
            em.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "✅ Data berhasil diperbarui!");
            loadTables();
        } catch (Exception e) {
            if (em.getTransaction().isActive()) em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "❌ Gagal update: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String[] options = {"Depo", "Perawatan"};
        int choice = JOptionPane.showOptionDialog(this, "Pilih tabel yang ingin dihapus:", "Delete",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
        if (choice == -1) return;

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPert12PU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            if (choice == 0) { // Depo
                int row = jTable1.getSelectedRow();
                if (row == -1) {
                    JOptionPane.showMessageDialog(this, "⚠️ Pilih baris Depo yang ingin dihapus!");
                    return;
                }
                int idDepo = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
                Depokeretasbi depo = em.find(Depokeretasbi.class, idDepo);
                if (depo != null) {
                    // hapus perawatan terkait dulu
                    List<PerawatanKereta> perawatanList = em.createQuery("SELECT p FROM PerawatanKereta p WHERE p.idArmada.idArmada=:id", PerawatanKereta.class)
                            .setParameter("id", idDepo)
                            .getResultList();
                    for (PerawatanKereta p : perawatanList) {
                        em.remove(p);
                    }
                    em.remove(depo);
                }

            } else { // Perawatan
                int row = jTable2.getSelectedRow();
                if (row == -1) {
                    JOptionPane.showMessageDialog(this, "⚠️ Pilih baris Perawatan yang ingin dihapus!");
                    return;
                }
                int idPerawatan = Integer.parseInt(jTable2.getValueAt(row, 0).toString());
                PerawatanKereta perawatan = em.find(PerawatanKereta.class, idPerawatan);
                if (perawatan != null) em.remove(perawatan);
            }

            em.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "✅ Data berhasil dihapus!");
            loadTables();

        } catch (Exception e) {
            if (em.getTransaction().isActive()) em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "❌ Gagal hapus: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

