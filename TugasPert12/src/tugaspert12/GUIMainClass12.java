    private void loadTables() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("TugasPert12PU");
        EntityManager em = emf.createEntityManager();

        try {
            // =======================
            // Load Depokeretasbi
            // =======================
            List<Depokeretasbi> depoList = em.createQuery("SELECT d FROM Depokeretasbi d", Depokeretasbi.class)
                    .getResultList();
            modelDepo = new DefaultTableModel(
                    new Object[]{"ID", "Nomor Armada", "Jenis Armada", "Tanggal Mulai Dinas"}, 0);
            for (Depokeretasbi d : depoList) {
                modelDepo.addRow(new Object[]{d.getIdArmada(), d.getNomorArmada(), d.getJenisArmada(), d.getTanggalMulaiDinas()});
            }
            jTable1.setModel(modelDepo);

            // =======================
            // Load PerawatanKereta
            // =======================
            List<PerawatanKereta> perawatanList = em.createQuery("SELECT p FROM PerawatanKereta p", PerawatanKereta.class)
                    .getResultList();
            modelPerawatan = new DefaultTableModel(
                    new Object[]{"ID Perawatan", "ID Armada", "Tanggal Perawatan", "Jenis Perawatan", "Keterangan"}, 0);
            for (PerawatanKereta p : perawatanList) {
                modelPerawatan.addRow(new Object[]{
                        p.getIdPerawatan(),
                        p.getIdArmada() != null ? p.getIdArmada().getIdArmada() : null,
                        p.getTanggalPerawatan(),
                        p.getJenisPerawatan(),
                        p.getKeterangan()
                });
            }
            jTable2.setModel(modelPerawatan);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Gagal load data: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }

